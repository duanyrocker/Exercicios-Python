"""
5- Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês.
Calcule e mostre o total do seu salário no referido mês.
"""
valor_hora = float(input("Por favor digite quanto você ganha por hora:"))
horas_trabalhadas = float(input("Digite o número de horas trabalhadas esse mês:"))
resultado = valor_hora * horas_trabalhadas
print(f"O total do sálario nesse mês é: {resultado}")


